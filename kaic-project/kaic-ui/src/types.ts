// Временная запись в списке задач, пока настоящий Task ещё не пришёл
// от backend'а — используется только для оптимистичного обновления
// интерфейса сразу после нажатия "Создать задачу". Не путать с Task
// из controlCenter.ts — это чисто локальная, ничего не значащая для
// backend'а сущность.
export interface PendingTask {
  id: string;
  textPreview: string;
  /** Реальный id задачи, как только backend его вернул из POST /tasks.
   * Пока запрос ещё в полёте — не задан, и TaskList не пытается искать
   * совпадение. Как только появился — это надёжный, однозначный признак
   * "вот эта самая задача пришла", в отличие от сравнения по тексту. */
  realTaskId?: string;
}
